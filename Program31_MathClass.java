//Write a java program that uses any 5 methods of "Math" class.
package Prac;

import java.util.*;
public class Exp31 {

	public static void main(String[] args) {
		
		int a=Math.floorDiv(50, 9);
		System.out.println(a);
		
		double b=Math.sqrt(121);
		System.out.println(b);
		
		double c=Math.log10(21);
		System.out.println(c);
		
		double d=Math.min(10, 20);
		System.out.println(d);
		
		int e=Math.decrementExact(2142);
		System.out.println(e);
		
		double f=Math.random();
		System.out.println(f*100);
		
		double g=Math.pow(4, 3);
		System.out.println(g);
	}

}
